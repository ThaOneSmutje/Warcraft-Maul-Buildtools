    public static byId(id: number): OrderId {
        if (id - 850000 < 0) {
            Log.Error(`unknown OrderId: ${id}`);
        }

        const ret: OrderId = OrderId.arr[id - 850000];

        if (ret === undefined) {
            Log.Error(`unknown OrderId: ${id}`);
        }

        return ret;
    }

